function sumar (a, b){
    return a + b
}

function restar (a, b){
    return a - b
}

function multiplicar (a, b){
    return a * b
}

function dividir (a, b){
    return a / b
}

let resultado;

resultado = sumar (1, 2);
console.log('la suma es :' + resultado);

resultado = restar (6, 2);
console.log('la resta es :' + resultado);

